def get_courses(skill: str) -> list[dict]:
    """Get course recommendations for a skill."""
    skill_lower = skill.lower()
    return COURSE_DB.get(skill_lower, DEFAULT_COURSES)


def get_all_course_recommendations(missing_skills: list[str]) -> dict:
    """Get course recommendations for all missing skills."""
    recommendations = {}
    for skill in missing_skills[:8]:
        recommendations[skill] = get_courses(skill)
    return recommendations


def get_platform_color(platform: str) -> str:
    """Get brand color for a platform."""
    return PLATFORM_COLORS.get(platform, PLATFORM_COLORS["default"])

